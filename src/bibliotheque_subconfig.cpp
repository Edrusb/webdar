using namespace std;


void bibliotheque_subconfig::subconfig_add_to(bibliotheque::using_set & added_to, const bibliotheque::using_set & to_add)
{
    for(bibliotheque::using_set::const_iterator it = to_add.begin();
	it != to_add.end();
	++it)
	added_to.insert(*it);
}

